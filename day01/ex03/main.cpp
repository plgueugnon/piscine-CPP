#include "Weapon.hpp"
#include "HumanA.hpp"
#include "HumanB.hpp"

/* in both cases we have to take the address of the object otherwise it would be created twice */

int	main( void ) {
	{
		Weapon club = Weapon("crude spiked club");
		HumanA bob("Bob", club);
		bob.attack();
		club.setType("some other type of club");
		bob.attack();
	}

	{
		Weapon club = Weapon("crude spiked club");
		HumanB jim("Jim");
		jim.setWeapon(club);
		jim.attack();
		club.setType("some other type of club");
		jim.attack();
	}

	return ( 0 );
}